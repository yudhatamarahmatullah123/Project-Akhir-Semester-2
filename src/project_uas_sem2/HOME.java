       void ubahTambah(){
               int ubah =table.getSelectedRowCount();
        
         if (ubah > 0) {
            
            int pil;
            pil = JOptionPane.NO_OPTION;
            pil = JOptionPane.showConfirmDialog(rootPane, "Apakah Anda Ingin Mengubah?", "Informasi", 
                                                                       JOptionPane.YES_NO_OPTION);
            if (pil == JOptionPane.YES_OPTION) {
                int row = table.getSelectedRow();
                
                nama.setText(table.getValueAt(row, 0).toString());
                JenisKelamin.setSelectedItem(table.getValueAt(row, 1));
                U.setText(table.getValueAt(row, 2).toString());
                T.setText(table.getValueAt(row, 3).toString());
                BB.setText(table.getValueAt(row, 4).toString());
                BBI.setText(table.getValueAt(row, 5).toString());
            }
            kuy =false;
        } else {
            JOptionPane.showMessageDialog(rootPane, "Silahkan Masukkan Data Dahulu!");
        }
    }
       

        void muncul(){
        dataModel.setRowCount(0);
        table.setModel(dataModel);
        String sem = "";
        try {
            Scanner A = new Scanner(new File(dat));
            while(A.hasNext()){
                sem += A.nextLine();
            }
            String pisah[] = sem.split("|");
            for (int i = 0; i < pisah.length; i++) {
                String pisah_kolom[]=pisah[i].split("-");
                for (int j = 0; j < pisah_kolom.length; j++) {
                    list.add(pisah_kolom[j]);
                }
                dataModel.addRow(list.toArray());
                list.clear();
            }
        } catch (Exception e) {
        }
    }
        
        
